    int valorMax(int[] vector) {
       int max;
        max = vector[0];
        
        for(int i = 0; i < vector.length; i++){
            //sacamos el maximo
            if(vector[i] > max){
                max = vector[i];
            }
        }
        return max;
    }
    int valorMin(int[] vector) {
       int min;
        min = vector[0];
        
        for(int i = 0; i < vector.length; i++){
            //sacamos el minimo
            if(vector[i] < min){
                min = vector[i];
            }
        }
        return min;
    }
